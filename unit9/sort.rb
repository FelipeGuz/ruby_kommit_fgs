## Sort the values in a array

numbers = [5,13,-2,8]
words = ["caterpillar","kangaroo","apple","zebra"]

## Sorting values (ascending)
puts
p numbers.sort
p words.sort

## Sorting values (descending)
puts
p numbers.sort.reverse
p words.sort.reverse

## bang method
puts
p numbers
p words

p numbers.sort!
p words.sort!

p numbers
p words